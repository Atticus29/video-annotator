export function isValidPassword(password: string): boolean {
  if (password) {
    const re: RegExp = new RegExp(
      "^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d@$!%*#?&]{7,}$"
    );
    return re.test(password.toLowerCase());
  } else {
    return false;
  }
}

